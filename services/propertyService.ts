export const getPropertiesForSeller = async (sellerId: string): Promise<Property[]> => {
    console.log(`Fetching properties for seller: ${sellerId}`);
    return new Promise(resolve => {
        setTimeout(() => {
            const sellerProperties = dummyProperties.filter(p => p.sellerId === sellerId);
            resolve(sellerProperties);
        }, 500);
    });
};

export const updateProperty = async (propertyData: Property): Promise<Property> => {
    return new Promise(resolve => {
        setTimeout(() => {
            const index = dummyProperties.findIndex(p => p.id === propertyData.id);
            if (index !== -1) {
                dummyProperties[index] = propertyData;
            }
            resolve(propertyData);
        }, 300);
    });
};